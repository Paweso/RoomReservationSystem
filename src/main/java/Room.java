public class Room {

    int number;
    int beds;

    public Room(int number, int beds) {
        this.number = number;
        this.beds = beds;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public int getBeds() {
        return beds;
    }

    public void setBeds(int beds) {
        this.beds = beds;
    }
}
